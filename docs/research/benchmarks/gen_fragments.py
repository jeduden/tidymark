#!/usr/bin/env python3
"""Regenerate the benchmark doc fragments from hyperfine JSON.

Single source of truth for the numbers the docs <?include?>.
Both run.sh (after a fresh benchmark) and the `bench-fragments`
CI gate call this with the committed JSON, so a hand-edited or
stale fragment fails `git diff --exit-code` in CI.

Usage: gen_fragments.py <json-dir> <fragment-dir>

Tables are emitted minimally padded; `mdsmith fix` normalizes
them to the canonical table form, which is also what the gate
compares against.
"""
import json
import pathlib
import sys

GEN = ("<!-- Generated by docs/research/benchmarks/gen_fragments.py "
       "from\ndocs/research/benchmarks/data/*.json — do not edit by "
       "hand. Re-run\nthe harness (run.sh) and `mdsmith fix` to "
       "refresh. -->\n")


def rows(json_dir: pathlib.Path, corpus: str):
    data = {}
    for name in (f"{corpus}.json", f"{corpus}_mdl.json"):
        p = json_dir / name
        if not p.exists():
            continue
        for r in json.load(open(p))["results"]:
            data[r["command"]] = (r["median"] * 1000, r["min"] * 1000)
    if not data:
        sys.exit(f"no JSON results for corpus {corpus!r} in {json_dir}")
    fastest = min(v[0] for v in data.values())
    order = sorted(data.items(), key=lambda kv: kv[1][0])
    lines = ["| Tool | Median | Min | vs mado |",
             "|---|---|---|---|"]
    for tool, (med, mn) in order:
        ratio = med / fastest
        rs = f"{ratio:.1f}x" if ratio < 10 else f"{round(ratio)}x"
        lines.append(f"| {tool} | {round(med)} ms | "
                     f"{round(mn)} ms | {rs} |")
    return "\n".join(lines), data


def main() -> None:
    if len(sys.argv) != 3:
        sys.exit(__doc__)
    json_dir = pathlib.Path(sys.argv[1])
    frag_dir = pathlib.Path(sys.argv[2])

    repo_tbl, repo = rows(json_dir, "corpus_repo")
    neut_tbl, _ = rows(json_dir, "corpus_neutral")

    note = ("`mdsmith` is the default rule set; `mdsmith-parity` "
            "disables the\nmdsmith-only rules so the work class "
            "matches the markdownlint\ntools (see "
            "`bench-parity.mdsmith.yml`).\n\n")
    results = (GEN + "\n" + note +
               "**Repo corpus — 523 Markdown files** (median wall "
               "time, lower is\nbetter; `vs mado` is the median "
               "ratio to the fastest tool):\n\n" + repo_tbl + "\n\n"
               "**Neutral corpus — 234 files** (Rust Book + Rust "
               "Reference,\nlonger third-party prose):\n\n"
               + neut_tbl + "\n")
    (frag_dir / "results.fragment.md").write_text(results)

    ms = repo.get("mdsmith", (0, 0))[0]
    ml = repo.get("markdownlint-cli2", (0, 0))[0]
    # Floor, not round: the headline must never overstate the win.
    mult = int(ml / ms) if ms else 0
    head = (GEN +
            "One static Go binary checks 523 Markdown files in "
            f"about\n{ms / 1000:.1f} s. That is roughly {mult}x "
            "faster than Node markdownlint.\nIt does more per file "
            "than the Rust linters; closing the rest of\nthat gap "
            "is active work.\n")
    (frag_dir / "headline.fragment.md").write_text(head)
    print(f"fragments regenerated in {frag_dir}")


if __name__ == "__main__":
    main()
